from __future__ import annotations

from dataclasses import dataclass


@dataclass
class MotionQueueStatus:
    """Структура, представляющая текущее состояние буфера команд движения в
    режиме реального времени.

    Инкапсулирует метаданные из RTD-потока, позволяющие отслеживать заполненность
    очереди траекторий и синхронизировать отправку новых команд.

    Attributes:
        available_slots (int): Количество свободных слотов в очереди команд.
            Определяет, сколько новых точек можно безопасно отправить без
            риска потери данных. Максимальное значение ограничено 16.
        received_count (int): Циклический счётчик (по модулю 256) принятых
            контроллером точек. Позволяет отслеживать прогресс выполнения очереди
            и детектировать аномалии или потерю пакетов в потоке реального времени.
        timestamp (Tuple[int, int]): Временная метка получения статуса в формате
            `(секунды, наносекунды)`. Предоставляется для синхронизации
            с внешними системами и расчёта задержек между отправкой команд
            и их обработкой роботом.
    """

    available_slots: int
    received_count: int
    timestamp: tuple[int, int]

    def __post_init__(self):
        self.received_count = self.received_count % 256
